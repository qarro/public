#题目：一个整数，它加上100后是一个完全平方数，再加上168又是一个完全平方数，请问该数是多少？
#假设这个整数位x，它加上100后的完全平方数是n^2
#再加上168后的完全平方数是m^2
#设i= m+n,j=m-n
#m^2 -n^2 = (m+n)(m-n）= 168
for i in range(1,85):
    if 168 % i == 0:
        j = 168/i;
        if i > j and (i+j)%2==0 and (i-j)%2==0:
            m = (i+j)/2
            n = (i-j)/2
            x = n * n -100
            print(x)